package aate.gob.pe.DTO;

public class FuncionalidadDTO {

	private String FUNNOM;
	private String FUNSIG;
	


	public FuncionalidadDTO(String fUNNOM, String fUNSIG) {
		super();
		FUNNOM = fUNNOM;
		FUNSIG = fUNSIG;
	}

	public String getFUNNOM() {
		return FUNNOM;
	}

	public void setFUNNOM(String fUNNOM) {
		FUNNOM = fUNNOM;
	}

	public String getFUNSIG() {
		return FUNSIG;
	}

	public void setFUNSIG(String fUNSIG) {
		FUNSIG = fUNSIG;
	}


	
	
	
	

	
	
	
}
